import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    passwordHash: {
      type: String,
      required: true,
    },
    otp: {
      type: String,
      default: null,
    },
    otpExpiresAt: {
      type: Date,
      default: null,
    },
    restePasswordSession: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true }
);

export const Users = mongoose.models.User || mongoose.model("User", userSchema);
